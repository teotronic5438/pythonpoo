def sumar(op1, op2):
    print(f"El resultado de la suma ({op1} + {op2}) es: ", op1+op2)

def restar(op1, op2):
    print(f"El resultado de la restar ({op1} - {op2}) es: ", op1-op2)

def multiplicar(op1, op2):
    print(f"El resultado de la multiplicacion ({op1} x {op2}) es: ", op1*op2)

def division(op1, op2):
    print(f"El resultado de la division ({op1} / {op2}) es: ", op1/op2)