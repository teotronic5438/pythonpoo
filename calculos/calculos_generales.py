def sumar(op1, op2):
    print("El resultado de la sumas es: ", op1+op2)

def restar(op1, op2):
    print("El resultado de la restar es: ", op1-op2)

def multiplicar(op1, op2):
    print("El resultado de la multiplicacion es: ", op1+op2)

def division(op1, op2):
    print("El resultado de la division es: ", op1/op2)

def potencia(op1, op2):
    print("El resultado de la potencia es: ", op1**op2)

def redondear(numero):
    print("El resultado de la redondear es: ", round(numero))